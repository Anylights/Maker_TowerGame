-- ============================================================================
-- LevelData.lua — 手动设计的关卡路径数据
-- ============================================================================
-- 每个关卡包含 1~3 条路径，每条路径由一系列航点 (waypoints) 构成。
-- 怪物从第一个航点出发，沿航点行进，最终到达能源塔中心 (0,0)。
-- 路径宽度 (width) 表示怪物可在中心线两侧随机分布的范围（半宽）。
--
-- 格式:
--   paths[i] = {
--       width = 10,              -- 路径总宽度（格）: 怪物在中心线 ± width/2 范围内分布
--       waypoints = {            -- 航点列表（从起点到终点）
--           { x = ..., z = ... },
--           ...
--       },
--   }
-- ============================================================================

