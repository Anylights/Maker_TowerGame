-- ============================================================================
-- 工具函数
-- ============================================================================

--- 获取关卡数据（超出范围则循环）
--- @param level number 关卡编号 (1-based)
--- @return table 关卡数据
function M.GetLevel(level)
    local maxLevel = #M
    local idx = ((level - 1) % maxLevel) + 1
    return M[idx]
end

--- 获取总关卡数
function M.GetMaxLevel()
    return #M
end

return M
